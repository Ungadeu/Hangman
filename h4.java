public class h4 {

    public static void main(String[] args){
        // declare and print a String variable
        String letter ="123";
        System.out.println(letter);
        System.out.println();

        //change a variable
        letter = "a";
        System.out.println("String variable:" + letter);
        System.out.println();

        //declare an integer
        int i = 0;
        System.out.println("Integer(int) variable:" + i);
        System.out.println();

        //change an integer
        i = 3 * (2 + 1) ;
        System.out.println("Integer(int) variable:" + i);

    }

}
